int TempConvert(int t, int BufLen) {
  int idx = 0;
  int value = 0;
  //Serial.print("T Value");
  //Serial.println(t);
  for (idx = 0; idx < BufLen; idx++) {
    //Serial.println(AT_TempLUT[idx][0]);
    if (AT_TempLUT[idx][0] == t) {
      value = AT_TempLUT[idx][1];
      break;
    }
  }
  //Serial.println(value);
  return value;
}